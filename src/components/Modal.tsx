import React from "react";

interface ModalProps {
  open: boolean;
  onClose: () => void;
  children: React.ReactNode;
}

const Modal: React.FC<ModalProps> = ({ open, onClose, children }) => {
  if (!open) return null;
  return (
    <div className="fixed inset-0 z-50 flex items-center justify-center bg-black/50">
      <div className="bg-white rounded-lg p-6 shadow-lg w-full max-w-md relative text-black">
        <button
          className="absolute top-2 right-2 text-[#CC0000] text-2xl font-bold"
          onClick={onClose}
          aria-label="Close"
        >
          Ã—
        </button>
        {children}
      </div>
    </div>
  );
};

export default Modal;
